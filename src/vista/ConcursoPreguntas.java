    private Pregunta obtenerPregunta() {

        return obtenerPreguntaRandom();

    }

    private Pregunta obtenerPreguntaRandom() {
         if (dificultad > 5) {
            JOptionPane.showMessageDialog(this, "Felicitaciones, ganaste el juego");
            ronda=5;
            guardar_Juego();
            resetearVarible();
        } else {
        int aleatorio = 0;
        aleatorio = (int) (Math.random() * 4);
        return listaPreguntas.stream().filter(f -> f.getDificultad().getId() == dificultad).collect(Collectors.toList()).get(aleatorio);
         }return null;
    }

    private void resetearVarible() {
        dificultad = 1;
        ronda = 1;
        premioAcumulado = 0;
        categoria = 1;
        btnOpcion1.setEnabled(false);
        btnOpcion2.setEnabled(false);
        btnOpcion3.setEnabled(false);
        btnOpcion4.setEnabled(false);
    }

    private void verificarRespuesta(String text) {
       

            int respuestaCorrecta = preguntaseleccionada.getRespuetaCorrecta();
            String textoRespuestaCorrecta = preguntaseleccionada.getRespuestas().stream().filter(f -> f.getId() == respuestaCorrecta).collect(Collectors.toList()).get(0).getOpcionesRespuesta();
            if (textoRespuestaCorrecta.equals(text)) {
                JOptionPane.showMessageDialog(this, "respuesta correcta");
                dificultad++;
                ronda++;
                premioAcumulado = premioAcumulado + preguntaseleccionada.getValorPremio();
                int result = JOptionPane.showConfirmDialog(this, "Desea continuar en el juego", "finalizar juego", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                if (result == JOptionPane.YES_OPTION) {
                    iniciarJuego();
                } else if (result == JOptionPane.NO_OPTION) {
                    guardar_Juego();
                    resetearVarible();
                }

            } else {
                JOptionPane.showMessageDialog(this, "respuesta incorrecta");
                guardar_Juego();
                lbAcumulado.setText("0");
                resetearVarible();
            }
        

    }

