    private void lista() {
        DefaultListModel model = new DefaultListModel();
        int index = 0;
        for (Juego juego : listaprincipal) {
            String data = juego.getJugador().getNombres() + " - " + juego.getPremioAcumulado() + " - " + juego.getRonda();
            model.add(index, data);
            index++;
        }
        jList1.setModel(model);

    }

